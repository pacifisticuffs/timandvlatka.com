                    <h2 class="section-heading text-center">Travel</h2>
                    <p class="text-muted">There are three major airports that serve the San Francisco Bay Area: San Francisco (SFO), Oakland (OAK), and San Jose (SJC). The most convenient is San Jose, leaving just a short drive to downtown. Better fares are sometimes available at SFO and OAK, but each is at least a 45 minute drive to San Jose, depending on traffic and how many other drivers you run off the road in a fit of uncontrollable rage.</p>

                    <h3 class="section-heading">Weather</h3>
                    <p class="text-muted">February in California can be hit or miss. Winter is our rainy season, with temps sometimes in the 50s, though it's not unusual to see a sunny day in the 70s. Come prepared for cool &amp; rainy, but pack some lightweight clothes just in case. Regardless of the season, evenings are almost always chilly with a light breeze.</p>
                    <span style="display: block !important; width: 320px; text-align: center; font-family: sans-serif; font-size: 12px;"><a href="http://www.wunderground.com/cgi-bin/findweather/getForecast?query=zmw:95112.1.99999&bannertypeclick=wu_clean2day" title="San Jose, California Weather Forecast" target="_blank"><img src="http://weathersticker.wunderground.com/weathersticker/cgi-bin/banner/ban/wxBanner?bannertype=wu_clean2day_cond&airportcode=KSJC&ForcedCity=San Jose&ForcedState=CA&zip=95112&language=EN" alt="Find more about Weather in San Jose, CA" width="300" /></a></span>

