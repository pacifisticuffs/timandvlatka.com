                            <dt><a href="http://www.thetech.org/" target="_blank">The Tech Museum</a></dt>
                            <dd>Tim used to work here! Some of the permanent exhibits are a little dated these days, but they have a great IMAX theater and traveling exhibits.</dd>
                            <dt><a href="http://sjmusart.org/" target="_blank">SJ Museum of Art</a></dt>
                            <dd>Just across the street from the Tech. They have some awesome exhibits that come through.</dd>
                            <dt><a href="https://www.yelp.com/biz/the-wooden-nickel-san-francisco" target="_blank">Wooden Nickel</a></dt>
