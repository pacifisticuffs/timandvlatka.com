                    <h3 class="section-heading">Getting Around</h3>
                    <div class="text-muted">
                        <p>San Jose Airport has a very convenient car rental facility just outside its terminals. If you prefer not to rent however, getting a cab, Uber or Lyft is an easy and generally affordable way of getting around town.</p>
                        <p>If you've never used Uber and you want to, please use my <a href="https://www.uber.com/invite/e2mjp" target="_blank">referral link</a>. We both get free rides! Similarly, you can use my <a href="https://www.lyft.com/invite/TIMOTHY22" target="_blank">referral code for Lyft</a> to get us both some free rides!</p>
                        <p>San Jose's downtown area is flat and easily walkable. There's also the <a href="http://www.vta.org/getting-around/interactive-light-rail-map" target="_blank">Lightrail</a> if you're into that whole train thing.</p>
                    </div>
                    <h3 class="section-heading">Stuff to Do</h3>
                    <div class="text-muted">
